namespace MAPIInspector.Parsers
{
    /// <summary>
    /// The enum type for days of the week.
    /// </summary>
    public enum DayOfWeek : byte
    {
        /// <summary>
        /// Sunday flag
        /// </summary>
        Sunday = 0x00,

        /// <summary>
        /// Monday flag
        /// </summary>
        Monday = 0x01,

        /// <summary>
        /// Tuesday flag
        /// </summary>
        Tuesday = 0x02,

        /// <summary>
        /// Wednesday flag
        /// </summary>
        Wednesday = 0x03,

        /// <summary>
        /// Thursday flag
        /// </summary>
        Thursday = 0x04,

        /// <summary>
        /// Friday flag
        /// </summary>
        Friday = 0x05,

        /// <summary>
        /// Saturday flag
        /// </summary>
        Saturday = 0x06
    }
}
