namespace MAPIInspector.Parsers
{
    /// <summary>
    /// The enum type for months of a year.
    /// </summary>
    public enum Month : byte
    {
        /// <summary>
        /// January flag
        /// </summary>
        January = 0x01,

        /// <summary>
        /// February flag
        /// </summary>
        February = 0x02,

        /// <summary>
        /// March flag
        /// </summary>
        March = 0x03,

        /// <summary>
        /// April flag
        /// </summary>
        April = 0x04,

        /// <summary>
        /// May flag
        /// </summary>
        May = 0x05,

        /// <summary>
        /// June flag
        /// </summary>
        June = 0x06,

        /// <summary>
        /// July flag
        /// </summary>
        July = 0x07,

        /// <summary>
        /// August flag
        /// </summary>
        August = 0x08,

        /// <summary>
        /// September flag
        /// </summary>
        September = 0x09,

        /// <summary>
        /// October flag
        /// </summary>
        October = 0x0A,

        /// <summary>
        /// November flag
        /// </summary>
        November = 0x0B,

        /// <summary>
        /// December flag
        /// </summary>
        December = 0x0C
    }
}
